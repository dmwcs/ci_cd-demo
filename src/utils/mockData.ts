const generatePressureData = (basePressure: number, pumpId: number) => {
  const pressureData = [];
  for (let hour = 0; hour <= 18; hour++) {
    const timestamp = new Date(
      `2024-07-15T${hour.toString().padStart(2, '0')}:00:00Z`,
    );

    const variation =
      Math.sin(hour * 0.3 + pumpId * 0.1) * 5 + (Math.random() - 0.5) * 3;
    const pressure = Math.round((basePressure + variation) * 10) / 10;
    pressureData.push({
      timestamp,
      pressure: Math.max(0, pressure),
    });
  }
  return pressureData;
};

